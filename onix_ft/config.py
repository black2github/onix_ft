# Размер одного блока данных в байтах.
# Увеличение → больше данных за одну отправку, но ближе к лимиту сообщения.
# Уменьшение → надёжнее, но медленнее.
# Текущее значение рассчитано под лимит Onix 4095 символов с XOR+base64.
# Не изменяйте без пересчёта (см. protocol.py).
CHUNK_BYTES: int = 3027

# Лимит символов в одном сообщении Onix (измерено экспериментально).
# Используется для проверки размера кадра при encode().
# Не изменяйте без проверки реального лимита мессенджера.
MESSAGE_MAX_LEN: int = 4095

# Размер окна — количество блоков, которые отправитель шлёт подряд
# не дожидаясь подтверждения. Получатель шлёт ACK раз в WINDOW_SIZE блоков.
# [СКОРОСТЬ] Увеличение ускоряет передачу, уменьшение повышает надёжность.
#   1  — классический stop-and-wait (максимальная надёжность)
#   5  — рекомендуемый старт (~2x ускорение)
#   10 — агрессивный режим (~3x ускорение)
WINDOW_SIZE: int = 10

# ==============================================================================
#  ТАЙМАУТЫ ОТПРАВКИ [СКОРОСТЬ + НАДЁЖНОСТЬ]
# ==============================================================================

# Задержка после нажатия «Отправить» перед следующим действием (секунды).
# [СКОРОСТЬ] Уменьшение ускоряет передачу, но Onix может не успеть
# обработать сообщение. Рекомендуемый диапазон: 0.5 — 1.5
SEND_DELAY: float = 0.5

# Как часто (секунды) скрипты проверяют наличие новых сообщений в чате.
# [СКОРОСТЬ] Уменьшение сокращает задержку получения ACK, но увеличивает
# нагрузку на браузер. Рекомендуемый диапазон: 0.2 — 2.0
# Значения ниже 0.2 нецелесообразны — браузер не успевает обновить DOM.
POLL_INTERVAL: float = 0.5

# Сколько раз повторить передачу окна при NACK или таймауте.
# [НАДЁЖНОСТЬ] Увеличение повышает устойчивость на нестабильном канале.
MAX_RETRIES: int = 5

# Секунд ждать ACK на всё окно блоков.
# [НАДЁЖНОСТЬ] Должен быть больше чем WINDOW_SIZE × SEND_DELAY + время
# распаковки архива на стороне получателя для последнего блока.
ACK_TIMEOUT: float = 120.0

# Секунд ждать META от отправителя при старте получателя.
# [НАДЁЖНОСТЬ] Даёт время запустить отправителя после получателя.
META_WAIT_TIMEOUT: float = 3600.0

# Секунд ждать очередной блок данных.
# [НАДЁЖНОСТЬ] При превышении получатель отправляет ABORT.
BLOCK_WAIT_TIMEOUT: float = 120.0

# ==============================================================================
#  ТАЙМАУТЫ БРАУЗЕРА [UI]
# ==============================================================================

# Ожидание появления элементов на странице (секунды).
ELEMENT_WAIT_TIMEOUT: float = 15.0

# Ожидание кнопки «Отправить» после вставки текста (секунды).
# Slate.js обновляет состояние кнопки асинхронно (~200-500 мс).
SEND_BTN_TIMEOUT: float = 2.0

# Ожидание готовности страницы при старте, включая время ручного логина.
PAGE_READY_TIMEOUT: float = 120.0

# Пауза после загрузки страницы для завершения lazy-loading истории чата.
# За это время старые сообщения появятся в DOM и будут помечены как виденные.
